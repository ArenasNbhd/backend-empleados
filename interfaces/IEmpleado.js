class IEmpleado {
    constructor (nombre, apaterno, amaterno, dirección, telefono, ciudad, estado, correo, noEmpleado, contrasena, perfil) {
        this.nombre = nombre
        this.apaterno = apaterno
        this.amaterno = amaterno
        this.dirección = direccion
        this.telefono = telefono
        this.ciudad = ciudad
        this.estado = estado
        this.correo = correo
        this.noEmpleado = noEmpleado
        this.contrasena = contrasena
        this.perfil = perfil
    }
}

export default IEmpleado